package com.unitx.signal_core.contract.position

/**
 * Screen position where the toast appears.
 * Configured via [ToastConfig.position].
 */
enum class ToastPosition {
    /** Toast appears near the top of the screen. */
    Top,
    /** Toast appears in the center of the screen. */
    Center,
    /** Toast appears near the bottom of the screen. */
    Bottom
}